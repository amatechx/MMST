    // Fungsi untuk generate konten dalam jumlah besar
    generateBulkContent: function(productName, category, format, language, platform, count) {
        let contents = [];
        for (let i = 1; i <= count; i++) {
            const content = this.generate(productName, category, format, language, platform);
            contents.push({
                filename: `product${i}.txt`,
                content: content
            });
        }
        return contents;
    },

    // Fungsi untuk download hasil generate sebagai ZIP
    downloadZip: async function(productName, category, format, language, platform, count) {
        try {
            // Generate konten
            const contents = this.generateBulkContent(productName, category, format, language, platform, count);
            
            // Buat objek ZIP
            const zip = new JSZip();
            
            // Tambahkan file ke ZIP
            contents.forEach(item => {
                zip.file(item.filename, item.content);
            });
            
            // Generate ZIP file
            const zipBlob = await zip.generateAsync({
                type: "blob",
                compression: "DEFLATE",
                compressionOptions: {
                    level: 9
                }
            });
            
            // Buat link download
            const downloadLink = document.createElement('a');
            downloadLink.href = URL.createObjectURL(zipBlob);
            downloadLink.download = `${productName}_contents.zip`;
            
            // Trigger download
            document.body.appendChild(downloadLink);
            downloadLink.click();
            document.body.removeChild(downloadLink);
            
            // Bersihkan URL
            URL.revokeObjectURL(downloadLink.href);
            
            return true;
        } catch (error) {
            console.error('Error generating ZIP:', error);
            return false;
        }
    }
